class Sum_of_two{
	public static void main(String[] args) {
		float a = 9;		// First number
		float b = 20;		// Second number
		float sum = a+b;	// sum of two number
		float ave = sum/2;	// Average of these two number
		System.out.println("The Sum of "+str(a)+" and "+b+" is = "+sum);
		System.out.print("The Average of "+a+" and "+b+" is = "+ave);
	}
}
